void Board::setPos(Position pos, char c) {
	if (board[pos.getY()][pos.getX()] == '*') breadCurmbs--;
	board[pos.getY()][pos.getX()] = c;
}

bool Board::isNoBreadCrumbs() {
	return (breadCurmbs == 0);
}